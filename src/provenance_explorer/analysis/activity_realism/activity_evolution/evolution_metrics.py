"""
Per-host activity evolution metrics from a first-seen ledger.
Expects an unnormalised (!) ledger (generated by ActivityEvolutionPlot)

Metrics
-------
n_unique_normalized_cmdlines : int
    Number of distinct command lines after normalisation.
saturation_auc_unit : float
    Area under the cumulative-unique-cmdlines curve projected onto the
    unit square (x = fraction of time elapsed, y = fraction of total
    unique cmdlines observed).
"""

from __future__ import annotations

import numpy as np
import pandas as pd

from provenance_explorer.analysis.activity_realism.activity_evolution.cmdline_normalization import (
    normalize,
)

BIN_WIDTH_NS = 5 * 60 * 10**9  # 5 minutes


def compute_activity_evolution_metrics(
    ledger: pd.DataFrame,
    ds: str,
    subds: str,
) -> pd.DataFrame:
    """
    Return one row per host with activity-evolution metrics.
    
    df with columns:
        host_id, n_unique_normalized_cmdlines, saturation_auc_unit
    """
    df = ledger.copy()
    df["normalised_cmdline"] = df["normalised_cmdline"].apply(
        lambda c: normalize(c, ds, subds)
    )

    # re-aggregate as some cmdlines may merge
    df = (
        df.groupby(["host_id", "normalised_cmdline"], as_index=False)
        .agg(first_seen_ns=("first_seen_ns", "min"))
    )

    results = []
    for host, hdf in df.groupby("host_id"):
        n_unique = len(hdf)

        # bin first-seen timestamps into 5-minute windows
        bins_ns = (hdf["first_seen_ns"].values // BIN_WIDTH_NS) * BIN_WIDTH_NS # type: ignore
        unique_bins, counts = np.unique(bins_ns, return_counts=True)
        cumulative = np.cumsum(counts).astype(float)

        if len(unique_bins) <= 1:
            auc = 1.0
        else:
            x = (unique_bins - unique_bins[0]) / (unique_bins[-1] - unique_bins[0])
            y = cumulative / cumulative[-1]
            auc = float(np.trapezoid(y, x)) # type: ignore

        results.append({
            "host_id": host,
            "n_unique_normalized_cmdlines": n_unique,
            "saturation_auc_unit": round(auc, 6),
        })


    return pd.DataFrame(results)
